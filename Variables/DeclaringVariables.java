package Variables;
/**
 * Declare three variables whose values are 63, 45.0 and 67.675586.
 */
public class DeclaringVariables {
    public static void main(String[] args) {
        int x = 63;
        float y = 45.0f;
        float z = 67.675586F;

        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("Z = " + z);

    }
}
